SELECT 
    HOHCOD AS CD_SP,
    CASE
    WHEN NVL(HOTRDJ,0)!='0' THEN TO_DATE((HOTRDJ+1900000), 'YYYYDDD')
    ELSE TO_DATE('01/01/1900','DD/MM/YYYY')
    END DATA_PEDIDO,
    CASE
    WHEN NVL(HOPDDJ,0)!='0' THEN TO_DATE((HOPDDJ+1900000), 'YYYYDDD')
    ELSE TO_DATE('01/01/1900','DD/MM/YYYY')
    END  DATA_ENTREGA,
    HODOCO AS N_PED,
    HOAN8 AS N_FORM,
    ABALPH AS FORNEC,
    SUM(SDAEXP/100) AS VALOR,
    HOKCOO AS CIA,
    SDPTC AS COND_PGT
FROM PRODDTA.F4209
INNER join PRODDTA.F4211 on SDDOCO = HODOCO
INNER JOIN PRODDTA.f0101 ON HOAN8 = ABAN8 
where HOHCOD = 'C1'
and HOAN8 = 3873
AND HOTRDJ > 124245
group by HOHCOD, HOTRDJ,HOPDDJ, HODOCO, HOAN8, ABALPH, HOKCOO, SDPTC;


